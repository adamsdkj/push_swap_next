#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*s_ptr;

	i = 0;
	s_ptr = s;
	while (i < n)
	{
		*s_ptr = '\0';
		i++;
		s_ptr++;
	}
}
