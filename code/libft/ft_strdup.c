#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*dup;
	size_t	size;

	size = ft_strlen(s);
	dup = malloc((size + 1) * sizeof(char));
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
